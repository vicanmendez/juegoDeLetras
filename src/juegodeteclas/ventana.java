  //Evento que controla que se ha soltado una tecla
    private void formKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyReleased
        //obtenemos la tecla presionada 
        char teclaPresionada = evt.getKeyChar();
         if(teclaPresionada == teclaSorteada) {
             aciertos++;
             lblAciertos.setText(aciertos + "");
         } else {
             intentos--;
             lblIntentos.setText(intentos + "");
         }
         //mostramos la tecla presionada debajo del panel principal
         String strTecla = Character.toString(teclaPresionada);
         strTecla = strTecla.toUpperCase();
         lblTeclaPresionada.setText(strTecla);
         
    }//GEN-LAST:event_formKeyReleased

