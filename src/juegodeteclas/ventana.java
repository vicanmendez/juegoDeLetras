    public void sortear() {
        lblTiempo.setText(TiempoRestante + "");
        aleatorio = (int) (Math.random() * arrayDeTeclas.length);
        //Seleccionamos el caracter sorteado
        teclaSorteada = arrayDeTeclas[aleatorio];
        System.out.println(teclaSorteada);
        lblTecla.setText(Character.toString(teclaSorteada));
       
        
    }
    //constructor de la ventana
    public ventana() {
        initComponents();
        //Luego de inicializar todos los componentes gráficos, tenemos que hacer algunas cuentas
       
        //Posicionamos el label con la tecla a presionar, encima del label que contiene la imagen
        lblTecla.setLocation(390, 192);
        getContentPane().setComponentZOrder(this.lblTecla, 0); 
       //Este timer se ejecuta por cada intervalo de tiempo definido en las variables globales
        new Timer().scheduleAtFixedRate(new TimerTask(){
            @Override
            public void run(){
                //Si nos queda tiempo restante o intentos, generamos otra tecla aleatoria y lo mostramos
                if((TiempoRestante > -1) && (intentos > 0)) {
                        sortear();
                } else {
                    //Si se terminó el juego, tenemos que parar el juego
                    this.cancel();
                    JOptionPane.showMessageDialog(null, "GAME OVER, terminaste con "+aciertos+" aciertos y "+intentos+" intentos");
                }
            }
        },0,IntervaloDeTiempo);
        
        //usamos otro timer que se ejecute cada 1000ms (1  segundo) para restar al tiempo de juego
        new Timer().scheduleAtFixedRate(new TimerTask(){
            @Override
            public void run(){
              TiempoRestante--;
              lblTiempo.setText(TiempoRestante+"");
            }
        },0,1000);
          
        
    }

